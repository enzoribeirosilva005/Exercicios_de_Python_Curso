# Operadores Aritméticos no Python

'''
+ Adição
- Subtração
* Multiplicação
/ Divisão
% Módulo/Resto da divisão
** Potência
// Divisão Inteira

Ordem de execução do python
1 - ()
2 - **
3 - * / // %
4 - + -

Exemplos abaixo: 
'''
print(5 + 3 * 2)
print(3 * 5 + 4**2)
print(3*(5 + 4)**2)
print(pow(4,3)) #Perde ordem de execução

print(81**(1/2)) #Forma de calcular a raiz de um número - Raiz é a potência dividida pelo meio

print('='*20) #Manipulação de strings
